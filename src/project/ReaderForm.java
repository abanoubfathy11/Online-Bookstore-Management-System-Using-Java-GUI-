        // TODO add your handling code here:
        DefaultListModel model=(DefaultListModel)bookcart.getModel();
        if(model.getSize()!=0){
            Reader reader;
            reader = f.SearchReader(signin_up.jTextField6.getText());
            
            //List< String> confirmlist=f.DisplayBooks();
           Object object=null;
            for (int i=0 ; i<model.getSize();i++)
            {
                object=model.getElementAt(i);
                Book b=f.Searchbooks(String.valueOf(object), "Available");
                
                if(b.getQuantity()>1){
                    f.AddNewSolidBook(reader.getEmail(), String.valueOf(object));
                    f.loadmapbook();
                    f.RemoveBook(b);
                    f.savebooks();
                    b.solidBook();
                    f.AddNewBook(b);
                    model.clear();
                    bookcart.setModel(model);
                }
                else{
                    
                    f.loadmapbook();
                    f.RemoveBook(b);
                    f.savebooks();
                    b.solidBook();
                    b.setStatusUnavailable();
                    f.AddNewBook(b);
                    model.clear();
                    bookcart.setModel(model);
                }
                
            }
             JOptionPane.showMessageDialog(rootPane, "Order Done :)");
             List <String > list1=f.DisplayCategory("Available");
        DefaultComboBoxModel mo=new DefaultComboBoxModel();
        for (String list11 : list1) {
            mo.addElement(list11);
        }
                 catagorybox.setModel(mo);
             bookbox.removeAllItems();
             autherbox.removeAllItems();
              
             
             costlabel.setText("0");
        }
        else
            JOptionPane.showMessageDialog(rootPane, "You must add book to cart !!");
